class User {
  String name;
  String email;
  String avatar;
  String grade;
  String institution;
  String occupation;
  String role;
  String aboutMe;

  User({
    required this.name,
    required this.email,
    required this.avatar,
    required this.grade,
    required this.institution,
    required this.occupation,
    required this.role,
    required this.aboutMe,
  });
}
